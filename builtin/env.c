#include "../header/minishell.h"

int	ft_env(t_shell *s)
{
	int	i;

	i = 0;
	while (s->env[i])
	{
		if (ft_strchr(s->env[i], '='))
			printf("%s\n", s->env[i]);
		i++;;
	}
	return (0);
}
